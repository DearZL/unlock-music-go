// KgmResult holds the decrypted audio bytes.
type KgmResult struct {
	Audio []byte
	Ext   string
	Mime  string
}

// DecryptKgm decrypts a Kugou .kgm/.kgma or .vpr file.
// isVpr should be true for .vpr files, false for .kgm/.kgma.
func DecryptKgm(data []byte, isVpr bool) (*KgmResult, error) {
	if len(data) < 0x2C {
		return nil, errors.New("kgm: file too short")
	}

	if isVpr {
		if !bytes.HasPrefix(data, vprMagicHeader) {
			return nil, errors.New("kgm: not a valid vpr file (bad magic)")
		}
	} else {
		if !bytes.HasPrefix(data, kgmMagicHeader) {
			return nil, errors.New("kgm: not a valid kgm file (bad magic)")
		}
	}

	headerLen := int(binary.LittleEndian.Uint32(data[0x10:0x14]))
	if headerLen > len(data) {
		return nil, errors.New("kgm: header length exceeds file size")
	}

	// Key is 16 bytes at 0x1C, with a 0x00 appended = 17 bytes
	key := make([]byte, 17)
	copy(key, data[0x1C:0x2C])
	key[16] = 0

	audioData := make([]byte, len(data)-headerLen)
	copy(audioData, data[headerLen:])

	var mask *[256]byte
	if isVpr {
		mask = &vprMask
	} else {
		mask = &kgmMaskV2
	}

	for i := range audioData {
		b := audioData[i]
		b ^= mask[i&0xFF]
		b ^= key[i%17]
		b = (b - mask[(i+1)&0xFF]) & 0xFF
		audioData[i] = b
	}

	ext := SniffAudioExt(audioData)
	return &KgmResult{
		Audio: audioData,
		Ext:   ext,
		Mime:  AudioMimeType(ext),
	}, nil
}
